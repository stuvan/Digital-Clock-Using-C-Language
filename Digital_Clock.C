int main()
{

    int hour, minute, second;
    int hour_first,hour_last;
    int minute_first,minute_last;
    int second_first,second_last; 

    time_t now;

    time(&now);

    struct tm *local = localtime(&now);


    hour = local->tm_hour;
    minute = local->tm_min;
    second = local->tm_sec;

                        
    while(1)
    {
        system("cls");
         
        hour_first = hour / 10;
        hour_last = hour % 10;

        minute_first = minute  / 10;
        minute_last = minute  % 10;

        second_first = second / 10;
        second_last = second % 10;
        printf("Digital Clock Using C Language");
        printf("\n_______________________________\n\n");

        show(hour_first,hour_last,minute_first,minute_last,second_first,second_last);

        fflush(stdout);
         
        second++;
 
        if(second>=60){
            minute+=1;
            second=0;
        }
        if(minute>=60){
            hour+=1;
            minute=0;
        }
        if(hour>=24){
            hour=0;
            minute=0;
            second=0;
        }

         sleep(1);
    }
 
    return 0;
}
